# Python code to demonstrate Implementing  
from collections import deque

# Queue using deque, which accepts a list as arguments
queue = deque(["Amar", "Akbar", "Anthony"])
queue.append("Ram") 
queue.append("Iqbal") 
print(queue) 
  
# Removes the first item 
print(queue.popleft()) 
  
print(queue.popleft) 
  
# Removes the first item 
print(queue.popleft()) 
  
print(queue) 
